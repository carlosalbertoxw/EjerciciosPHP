<?php

/**
 * Hola Mundo
 * ----------
 * La forma más simple de mostrar texto en PHP.
 * Existen dos formas: "echo" y "print". Ambas imprimen texto;
 * echo es la más común y puede recibir varios valores separados por comas.
 *
 * PHP_EOL es una constante que representa el "salto de línea" del sistema
 * operativo. La usamos para que el texto se vea ordenado en la terminal.
 */

echo 'Hola Mundo :)' . PHP_EOL;

print 'Hola Mundo otra vez :)' . PHP_EOL;

// echo puede recibir varios valores separados por comas:
echo 'Hola', ' ', 'Mundo', ' ', 'con echo', PHP_EOL;
